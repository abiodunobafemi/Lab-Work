// Write a Java program called MoreArithmetic using java.lang.Math classes that displays the following on the console.
// pi is 3.141592653589793
// pi * 2 is 6.283185307179586
// pi^2 is 9.869604401089358

public class MoreArithmetic {

	public static void main(String[] args) {
		
		// Displays the statements
		System.out.print("pi is ");
		System.out.println(Math.PI);
		System.out.print("pi * 2 is ");
		System.out.println(Math.PI * 2);
		System.out.print("pi^2 is ");
		System.out.println(Math.pow(Math.PI, 2));
	}

}
